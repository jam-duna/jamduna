fn empty_output() -> (u64, u64) {
    (FIRST_READABLE_ADDRESS as u64, 0)
}

fn leak_output(mut output_BUFFER: Vec<u8>) -> (u64, u64) {
    let ptr = output_BUFFER.as_mut_ptr() as u64;
    let len = output_BUFFER.len() as u64;
    core::mem::forget(output_BUFFER);
    (ptr, len)
}

#[polkavm_derive::polkavm_export]
extern "C" fn accumulate(start_address: u64, length: u64) -> (u64, u64) {
    // Parse accumulate args (GP 0.7.2 format)
    let Some(args) = parse_accumulate_args(start_address, length) else {
        call_log(1, None, "FIB accumulate: parse_accumulate_args failed");
        return empty_output();
    };

    if args.num_accumulate_inputs == 0 {
        call_log(1, None, "FIB accumulate: num_accumulate_inputs is zero");
        return empty_output();
    }

    // Fetch accumulate inputs
    let accumulate_inputs = match fetch_accumulate_inputs(args.num_accumulate_inputs as u64) {
        Ok(inputs) => inputs,
        Err(e) => {
            call_log(1, None, &format!("FIB accumulate: fetch_accumulate_inputs failed: {:?}", e));
            return empty_output();
        }
    };

    // Process each accumulate input
    for (i, input) in accumulate_inputs.iter().enumerate() {
        match input {
            AccumulateInput::OperandElements(operand) => {
                // Get work result data
                if let Some(ref result_data) = operand.result.ok {
                    // Write FIB result to storage using index as key
                    let key = [i as u8];
                    unsafe {
                        write(
                            key.as_ptr() as u64,
                            key.len() as u64,
                            result_data.as_ptr() as u64,
                            result_data.len() as u64,
                        );
                    }
                    call_log(2, None, &format!("FIB accumulate: stored result {} ({} bytes)", i, result_data.len()));
                }
            }
            AccumulateInput::DeferredTransfer(_transfer) => {
                // Handle deferred transfers if needed (fib service doesn't use these)
                call_log(2, None, "FIB accumulate: received deferred transfer (ignored)");
            }
        }
    }

    // Return a 32-byte output (commitment)
    let mut output = [0u8; 32];
    output[0] = args.num_accumulate_inputs as u8;
    leak_output(output.to_vec())
}